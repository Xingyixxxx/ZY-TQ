/**
 * 获取卦象信息
 * @param {string} binaryStr - 6位二进制字符串，如 '111111'
 * @returns {object} 卦象信息对象
 */
function getHexagramInfo(binaryStr) {
    const lowerCode = binaryStr.substring(0, 3);
    const upperCode = binaryStr.substring(3, 6);
    const lowerTrigram = TRIGRAMS[lowerCode] || { name: '未知', nature: '?', symbol: '?' };
    const upperTrigram = TRIGRAMS[upperCode] || { name: '未知', nature: '?', symbol: '?' };

    const data = HEXAGRAM_DATA[binaryStr];
    const order = HEXAGRAM_ORDER[binaryStr] || 1;
    return {
        lowerTrigram,
        upperTrigram,
        order,
        name:           data ? data[0] : `${upperTrigram.nature}${lowerTrigram.nature}卦`,
        summary:        data ? data[1] : '此卦深邃，静心参悟。',
        interpretation: data ? data[2] : '顺应自然，万物皆有变数，静待时机即可。'
    };
}
